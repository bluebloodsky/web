package com.whnr.cac.web.model;

public class DoPanel {
	private String xtype = "doTextPanel";
	private String doDesc;
	private String doName;
	private String doTime;
	private String doValue;
	private String doState;
	private String doUrl;
	public String getXtype() {
		return xtype;
	}
	public void setXtype(String xtype) {
		this.xtype = xtype;
	}
	public String getDoDesc() {
		return doDesc;
	}
	public void setDoDesc(String doDesc) {
		this.doDesc = doDesc;
	}
	public String getDoName() {
		return doName;
	}
	public void setDoName(String doName) {
		this.doName = doName;
	}
	public String getDoTime() {
		return doTime;
	}
	public void setDoTime(String doTime) {
		this.doTime = doTime;
	}
	public String getDoValue() {
		return doValue;
	}
	public void setDoValue(String doValue) {
		this.doValue = doValue;
	}
	public String getDoState() {
		return doState;
	}
	public void setDoState(String doState) {
		this.doState = doState;
	}
	public String getDoUrl() {
		return doUrl;
	}
	public void setDoUrl(String doUrl) {
		this.doUrl = doUrl;
	}
}
